public class ArrayTutorial
{
    public static void main(String[] args)
    {
        int a[] = new int[5];
        // 2 6 8 4 1
        // 0 1 2 3 4

        a[0] = 2;
        a[1] = 6;
        a[2] = 8;
        a[3] = 4;
        a[4] = 1;

        for (int i=0;i<5;i++)
            System.out.println(a[i]);


    }
}
